package com.pack1;

public class bitwise {
    public static void main(String[] args) {
        int a = 22;
        int b = 23;
        System.out.println("Bitwise And Operator " + (a & b));
        System.out.println("Bitwise OR Operator: " + (a | b));
        System.out.println("Left Shift :" +(a<<3));
    }
}
